#!/usr/bin/env python3
"""Derive one-row-per-patient analysis dataset from DuckDB database.

Generated by onc-data-wrangler derive-dataset skill.
Date: 2026-04-09
Source database: example-synthetic-data.duckdb
Output: analysis_dataset.csv

Columns derived:
  1. record_id          - Patient identifier
  2. age_at_diagnosis   - Age at diagnosis in years (integer, from cohort table)
  3. sex                - Sex mapped from NAACCR code (1=Male, 2=Female)
  4. cancer_type        - Cancer type free-text description (from prissmm extraction)
  5. stage_at_diagnosis - Stage at diagnosis (I-IV with substages, from prissmm extraction)
"""

import duckdb
import pandas as pd
from pathlib import Path

# ---- Configuration ----
DB_PATH = str(Path(__file__).parent / "example-synthetic-data.duckdb")
OUTPUT_PATH = str(Path(__file__).parent / "analysis_dataset.csv")


def derive_dataset(db_path: str) -> pd.DataFrame:
    """Derive the analysis dataset from the DuckDB database.

    Returns a one-row-per-patient DataFrame.
    """
    con = duckdb.connect(db_path, read_only=True)

    query = """
    WITH base AS (
        -- One row per patient from the cohort table
        SELECT DISTINCT record_id FROM cohort
    ),

    -- Age at diagnosis: integer cast from cohort demographics
    col_age AS (
        SELECT record_id,
               TRY_CAST(age_at_diagnosis AS INTEGER) AS age_at_diagnosis
        FROM cohort
    ),

    -- Sex: NAACCR sex code mapped to Male/Female
    -- NAACCR convention: 1 = Male, 2 = Female
    col_sex AS (
        SELECT record_id,
               CASE naaccr_sex_code
                   WHEN '1' THEN 'Male'
                   WHEN '2' THEN 'Female'
                   ELSE NULL
               END AS sex
        FROM cohort
    ),

    -- Cancer type: free-text description from the prissmm extraction category
    -- Takes the first non-null ca_type per patient (ordered by instance_index)
    col_cancer_type AS (
        SELECT record_id,
               ca_type AS cancer_type
        FROM (
            SELECT record_id, ca_type,
                   ROW_NUMBER() OVER (PARTITION BY record_id ORDER BY instance_index) AS rn
            FROM extractions
            WHERE category = 'prissmm'
              AND ca_type IS NOT NULL AND ca_type != 'nan' AND TRIM(ca_type) != ''
        ) sub
        WHERE rn = 1
    ),

    -- Stage at diagnosis: from prissmm extraction category
    -- Takes the first non-null ca_stage per patient (ordered by instance_index)
    col_stage AS (
        SELECT record_id,
               ca_stage AS stage_at_diagnosis
        FROM (
            SELECT record_id, ca_stage,
                   ROW_NUMBER() OVER (PARTITION BY record_id ORDER BY instance_index) AS rn
            FROM extractions
            WHERE category = 'prissmm'
              AND ca_stage IS NOT NULL AND ca_stage != 'nan' AND TRIM(ca_stage) != ''
        ) sub
        WHERE rn = 1
    )

    SELECT b.record_id,
           a.age_at_diagnosis,
           s.sex,
           ct.cancer_type,
           st.stage_at_diagnosis
    FROM base b
    LEFT JOIN col_age a ON b.record_id = a.record_id
    LEFT JOIN col_sex s ON b.record_id = s.record_id
    LEFT JOIN col_cancer_type ct ON b.record_id = ct.record_id
    LEFT JOIN col_stage st ON b.record_id = st.record_id
    """

    df = con.execute(query).fetchdf()
    con.close()

    # ---- Validation ----
    assert len(df) == df["record_id"].nunique(), (
        f"Expected one row per patient, got {len(df)} rows for "
        f"{df['record_id'].nunique()} patients"
    )

    return df


def main():
    print(f"Reading database: {DB_PATH}")
    df = derive_dataset(DB_PATH)
    Path(OUTPUT_PATH).parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(OUTPUT_PATH, index=False)
    print(f"Saved: {OUTPUT_PATH}")
    print(f"  {len(df)} rows x {len(df.columns)} columns")

    # Summary
    print(f"\nColumn summary:")
    for col in df.columns:
        n_miss = df[col].isna().sum()
        pct = 100.0 * n_miss / len(df)
        print(f"  {col:30s} {n_miss:>5d} missing ({pct:.1f}%)")


if __name__ == "__main__":
    main()
